// Operadores 

let a = 3, b = 2;
let z = a + b;
console.log(' Resultado de la suma: ' + z);

z = a - b;
console.log('Resultado de la resta: ' + z);

z = a * b;
console.log('Resultado de la multi: ' + z);

z = a / b;
console.log('Resultado de al div: ' + z);

z = a % b; 
console.log('El residuo de la div es : ' + z);

z = a ** b;
console.log('Resultado del operador exponente: '+ z);

// Incremento 
// Pre-Incremento (el operador ++ antes de la variable)
z = ++a;
console.log(z); 

